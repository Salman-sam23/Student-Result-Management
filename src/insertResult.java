    private void resultsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resultsaveActionPerformed
        // TODO add your handling code here:
        String rollNo=jTextField1.getText();
        String physics=jTextField2.getText();
        String maths=jTextField3.getText();
        String OS=jTextField4.getText();
        String DBMS=jTextField5.getText();
        String EM=jTextField6.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
    
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srmp", "root", "?Ishu0407");
            Statement st=con.createStatement();
            
            ResultSet rs=st.executeQuery("select * from srmp.student_details where rollNo='"+rollNo+"'");
            if(rs.next()){
                 st.executeUpdate("insert into srmp.result (rollNo, physics, maths, OS, DBMS, EM) values ('"+rollNo+"','"+physics+"','"+maths+"','"+OS+"','"+DBMS+"','"+EM+"')");
 
                JOptionPane.showMessageDialog(null, "Data updated Successfully!");
                setVisible(false);
                new insertResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Student is not registered!");
                 setVisible(false);
                new insertResult().setVisible(true);
            }
           
            
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(null, e.toString());
    }
    }//GEN-LAST:event_resultsaveActionPerformed

